package org.easylenium.core.xml.exception;

public class ConvertNodeToPojoException extends RuntimeException
{

	private static final long serialVersionUID = -174068610390415021L;

	public ConvertNodeToPojoException()
	{
		super();
	}

	public ConvertNodeToPojoException(String arg0, Throwable arg1, boolean arg2, boolean arg3)
	{
		super(arg0, arg1, arg2, arg3);
	}

	public ConvertNodeToPojoException(String arg0, Throwable arg1)
	{
		super(arg0, arg1);
	}

	public ConvertNodeToPojoException(String arg0)
	{
		super(arg0);
	}

	public ConvertNodeToPojoException(Throwable arg0)
	{
		super(arg0);
	}

}
